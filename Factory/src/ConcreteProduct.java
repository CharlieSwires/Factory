
public class ConcreteProduct implements Product{

    private int x,y,z;

    @Override
    public int getX() {
        return x;
    }

    @Override
    public void setX(int x) {
        this.x = x;
    }

    @Override
    public int getY() {
        return y;
    }

    @Override
    public void setY(int y) {
        this.y = y;
    }

    @Override
    public int getZ() {
        return z;
    }

    @Override
    public void setZ(int z) {
        this.z = z;
    }

    @Override
    public String toString() {
        return "ConcreteProduct [x=" + x + ", y=" + y + ", z=" + z + "]";
    }
    
}
